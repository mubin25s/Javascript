// Basic Math Problems
let num1: number = 20;
let num2: number = 10;

console.log(`Addition: ${num1 + num2}`);
console.log(`Subtraction: ${num1 - num2}`);
console.log(`Multiplication: ${num1 * num2}`);
console.log(`Division: ${num1 / num2}`);
console.log(`Remainder (Modulus): ${num1 % 3}`);
console.log(`Exponentiation: ${num1 ** 2}`);

// Simple problem: Calculate Area of Circle
const PI: number = 3.14159;
let radius: number = 5;
let area: number = PI * (radius ** 2);
console.log(`Area of Circle with radius ${radius}: ${area}`);
